<template>
    <v-layout row justify-center>
        <v-dialog
            v-model="displayModal"
            fullscreen 
            transition="dialog-bottom-transition" 
            :overlay=false
        >
            <v-card>
                <v-card-actions>
                    <v-spacer></v-spacer>
                    <v-btn flat icon @click="closeNewsView" class="red--text">
                        <v-icon>fa-times</v-icon>
                    </v-btn>
                </v-card-actions>
                <v-card-title>
                    <v-container v-if="item.title !== undefined">
                        <div class="display-1">
                            {{ item.title }}
                        </div>
                        <img 
                            :src="item.image" 
                            :alt="item.title" 
                            class="image-view"
                        />
                        <div class="title" v-html="item.description" style="margin-top: 35px; margin-bottom: 40px;">
                        </div>
                    </v-container>
                </v-card-title>
                <v-bottom-nav value="true" class="white">
                    <v-btn
                        flat
                        class="orange--text"
                        :value="true"
                        @click.stop="addNewsAsFavourite(item, index)"
                    >
                        <span>Favourite</span>
                        <v-icon>
                            {{ item.favourite ? 'fa-heart' : 'fa-heart-o' }}
                        </v-icon>
                    </v-btn>
                    <v-btn
                        flat
                        class="red--text"
                        :value="true"
                        target="_blank"
                        :href="item.URL"
                    >
                        <span>Open Link</span>
                        <v-icon>fa-external-link</v-icon>
                    </v-btn>
                </v-bottom-nav>         
            </v-card>
        </v-dialog>
    </v-layout>
</template>

<script>
    export default {
        props: {
            showModal: {
                type: Boolean,
                required: true
            },
            item: {
                type: Object,
                required: true
            },
            closeModal: {
                type: Function,
                required: true
            },
            addNewsAsFavourite: {
                type: Function
            },
            index: {
                type: Number,
                required: true
            }
        },
        data() {
            return {
                displayModal: false
            };
        },
        watch: {
            showModal(updatedValue) {
                if (updatedValue)
                    this.displayModal = updatedValue;
            }
        },
        methods: {
            closeNewsView() {
                this.displayModal = false;
                this.closeModal();
            }
        }
    }
</script>